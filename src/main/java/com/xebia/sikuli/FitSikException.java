package com.xebia.sikuli;

public class FitSikException extends RuntimeException {

    public FitSikException() {
    }

    public FitSikException(String message) {
        super(message);
    }

    public FitSikException(Throwable cause) {
        super(cause);
    }

    public FitSikException(String message, Throwable cause) {
        super(message, cause);
    }


}
